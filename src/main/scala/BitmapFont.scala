import scala.collection.immutable.ArraySeq

class BitmapFont(fnt: ArraySeq[Int]) {
  val ascent = 9
  val height = 12

  def lookup(idx: Int): Vector[Seq[Boolean]] = {
    fnt.slice(idx * height, (idx + 1) * height) map (b =>
      for (i <- 7 to 0 by -1) yield if (((b >> i) & 1) == 1) true else false) toVector
  }

  def show(idx: Int): Unit = println(lookup(idx) map (r => r map (c => if (c) '*' else ' ') mkString) mkString "\n")

  def show(char: Char): Unit = println(lookup(char) map (r => r map (c => if (c) '*' else ' ') mkString) mkString "\n")

}

